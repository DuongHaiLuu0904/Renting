    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if ((evt.getSource() instanceof JButton) && (((JButton) evt.getSource()).equals(btnLogin))) {
            RentalAgent agent = new RentalAgent();
            agent.setUsername(txtUsername.getText());
            agent.setPassword(new String(txtPassword.getPassword()));
            RentalAgentDAO rad = new RentalAgentDAO();
            if (rad.checkLogin(agent)) {
                (new ViewRentalAgnetFrm(agent)).setVisible(true);
                this.dispose();
                System.out.println("Login success");
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect username and/or password!");
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

